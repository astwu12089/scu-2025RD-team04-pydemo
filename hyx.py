# 1. 打印输出
print("Hello, World!")  # 经典入门语句

# 2. 变量与计算
a = 5
b = 3
sum_result = a + b
print(f"{a} + {b} = {sum_result}")  # 输出: 5 + 3 = 8

# 3. 列表操作
fruits = ["apple", "banana", "cherry"]
print("My favorite fruits:")
for fruit in fruits:
    print(f"- {fruit}")

# 4. 条件判断
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# 5. 函数定义
def greet(name):
    return f"Welcome, {name}!"

print(greet("Alice"))  # 输出: Welcome, Alice!